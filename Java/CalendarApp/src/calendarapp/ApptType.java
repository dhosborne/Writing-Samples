package calendarapp;

/**
 *
 * @author derekosborne
 */
public class ApptType {
    String type;
    Integer count;

    public ApptType(String type, Integer count) {
        this.type = type;
        this.count = count;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
    
    
}
